package com.zsgs.chandru.librarymanagement.credential;

public class LibrarianCredentials {
    private int librarianId;
    private String librarianIdPassword;

    public int getLibrarianId() {
        return librarianId;
    }

    public void setLibrarianId(int librarianId) {
        this.librarianId = librarianId;
    }

    public String getLibrarianIdPassword() {
        return librarianIdPassword;
    }

    public void setLibrarianIdPassword(String librarianIdPassword) {
        this.librarianIdPassword = librarianIdPassword;
    }
}
